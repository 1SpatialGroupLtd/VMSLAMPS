create table O.T_ELEMENT (
ELEMENT_ID    O.ELEMENT_ID,
FLOWLINE_ID   O.FLOWLINE_ID,
PROCEDURE_ID  O.PROCEDURE_ID,
COMMAND_PROC  O.COMMAND_PROC,
CONDITIONS    O.CONDITIONS,
PRIORITY      O.PRIORITY
);

create table O.T_FLOWLINE (
FLOWLINE_ID   O.FLOWLINE_ID,
START_ELEMENT O.ELEMENT_ID,
END_ELEMENT   O.ELEMENT_ID,
COMMAND_PROC  O.COMMAND_PROC,
PRIORITY      O.PRIORITY
);

create table O.T_OUTCOME (
FLOWLINE_ID   O.FLOWLINE_ID,
ELEMENT_ID    O.ELEMENT_ID,
OUTCOME_VALUE O.OUTCOME_VALUE,
WIRE_ID       O.WIRE_ID
);
 
create table O.T_PROCEDURE (
PROCEDURE_ID  O.PROCEDURE_ID,
COMMAND_PROC  O.COMMAND_PROC,
PRIORITY      O.PRIORITY
); 

create table O.T_WIRE (
FLOWLINE_ID   O.FLOWLINE_ID,
WIRE_ID       O.WIRE_ID,
WIRE_END      O.ELEMENT_ID
);

